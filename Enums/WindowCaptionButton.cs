

namespace System.Windows.Forms
{
    /// <summary>
    /// 鼠标在按钮上的状态
    /// </summary>
    public enum ButtonMouseState
    {
        /// <summary>
        /// 鼠标无状态
        /// </summary>
        Normal = 0,

        /// <summary>
        /// 鼠标悬浮
        /// </summary>
        Hover = 1,

        /// <summary>
        /// 鼠标按下
        /// </summary>
        Down = 2,
        /// <summary>
        /// 鼠标抬起
        /// </summary>
        Up = 4
    }

}
